public class Student {
  private String name;
  private int id;
  private double cgpa;
  public void setName(String n) {
    name=n;
  }
  public String getName() {
    return name;
  }
  public void setID(int i) {
    id=i;
  }
  public int getID() {
    return id;
  }
  public void setCGPA(double cg) {
    cgpa=cg;
  }
  public double getCGPA() {
    return cgpa;
  }
}